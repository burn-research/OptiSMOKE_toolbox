#include "rhoVDI.h"
#include <cmath>

RhoVDI::RhoVDI(double* coefficients)
:
DensityModel()
{
  _coefficients = new double[4];

  for (unsigned int i = 0; i < 4; i++)
    {
      _coefficients[i] = coefficients[i];
    }
}

RhoVDI::~RhoVDI()
{
  delete [] _coefficients;
}


double RhoVDI::rho(double T, double P)
{
	double Teff = min(T, 0.999*_coefficients[2]);
  return _coefficients[0] / (pow(_coefficients[1], 1.0 + pow(1.0 - Teff / _coefficients[2], _coefficients[3])));
}
