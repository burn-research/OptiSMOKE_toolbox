SurfaceTensionModel::SurfaceTensionModel(double* coefficients, double Tc)
:
_Tc(Tc)
{
    _coefficients = new double[2];
    
    for (unsigned int i=0; i<2; i++)
    {
        _coefficients[i] = coefficients[i];
    }
}

double SurfaceTensionModel::sigma(double T)
{
    return 1e-03*_coefficients[0]*pow(1.0 - min(T, _Tc)/_Tc, _coefficients[1]);
}
