DensityModel::DensityModel()
{

}

double DensityModel::d_rhoL_over_dT(double T, double P)
{
  double d_density_d_T;
  double eps_dT = 1.e-06;
  double rho_l_plus, rho_l_minus;
  double T_old = T;
  double T_plus_eps = T + eps_dT;
  double T_minus_eps = T - eps_dT;

  rho_l_plus = this->rho(T_plus_eps, P);
  rho_l_minus = this->rho(T_minus_eps, P);

  d_density_d_T = (rho_l_plus - rho_l_minus) / (T_plus_eps - T_minus_eps);

  return d_density_d_T;
}
